def compute_text_complexity(text):
    tokens = tokenize_text(text)
    unique_tokens = len(set(tokens))
    avg_token_length = np.mean([len(t) for t in tokens]) if tokens else 1.0
    return unique_tokens / len(tokens) if tokens else 0.0, avg_token_length

def tokenize_text(text, complexity=0.5):
    if complexity > 0.7:
        tokens = re.findall(r'\w+|[^\w\s]', text, re.UNICODE)
    else:
        tokens = text.split()
    return tokens

def preprocess_text(text, complexity=None):
    if not isinstance(text, str) or not text.strip():
        return "[EMPTY]"
    for emoji_char, emotion_word in emoji_map.items():
        text = text.replace(emoji_char, f" {emotion_word} ")
    text = emoji.demojize(text, language='ar')
    text = re.sub(r'http\S+|www.\S+', '', text)
    text = re.sub(r'#\w+', '', text)
    complexity_score, avg_token_length = compute_text_complexity(text) if complexity is None else (complexity, 1.0)
    tokens = tokenize_text(text, complexity_score)
    text = " ".join(tokens)
    text = re.sub(r'[^\w\s\U0001F600-\U0001F64F]', '', text)
    text = re.sub(r'\s+', ' ', text).strip()
    return text if text else "[EMPTY]"

def preprocess_data(df):
    df = df.copy()
    df.columns = df.columns.str.strip()
    logger.info(f"Dataset columns: {list(df.columns)}")
    print(f"Dataset columns: {list(df.columns)}")
    if 'content' not in df.columns:
        raise ValueError("Expected 'content' column in dataset")
    df = df.dropna(subset=['content']).reset_index(drop=True)
    tokenizer = AutoTokenizer.from_pretrained("UBC-NLP/MARBERT", use_fast=True, cache_dir='./cache')

    processed_texts = []
    valid_indices = []
    for idx, text in enumerate(tqdm(df['content'], desc="Preprocessing texts")):
        processed_text = preprocess_text(text)
        if processed_text == "[EMPTY]":
            logger.warning(f"Text at index {idx} is empty after preprocessing")
            continue
        enc = tokenizer(
            processed_text,
            padding=False,
            truncation=True,
            max_length=CONFIG['max_length'],
            return_tensors='pt'
        )
        input_ids = enc['input_ids'][0]
        valid_tokens = input_ids.ne(tokenizer.pad_token_id).sum().item() - 2
        if valid_tokens < CONFIG['min_valid_tokens']:
            logger.warning(f"Text at index {idx}: '{processed_text}' has too few valid tokens ({valid_tokens})")
            continue
        processed_texts.append(processed_text)
        valid_indices.append(idx)

    df = df.iloc[valid_indices].reset_index(drop=True)
    df['content'] = processed_texts

    emotion_cols = CONFIG['emotion_labels']
    for col in emotion_cols:
        if col not in df.columns:
            raise ValueError(f"Missing emotion column: {col}")
        df[col] = pd.to_numeric(df[col], errors='coerce').fillna(0).astype(int)
        logger.info(f"{col} unique values after preprocessing: {df[col].unique()}")
        print(f"{col} unique values after preprocessing: {df[col].unique()}")
    no_labels = df[emotion_cols].sum(axis=1) == 0
    logger.info(f"Samples with no labels before filtering: {no_labels.sum()}")
    print(f"Samples with no labels before filtering: {no_labels.sum()}")
    df = df[~no_labels].reset_index(drop=True)
    logger.info(f"Samples with no labels after filtering: {(df[emotion_cols].sum(axis=1) == 0).sum()}")
    print(f"Samples with no labels after filtering: {(df[emotion_cols].sum(axis=1) == 0).sum()}")
    return df

