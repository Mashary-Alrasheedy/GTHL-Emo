def train_model(model, train_texts, val_texts, train_labels, val_labels, weights_file):
    print("Training full model...")
    logger.info("Training model")
    optimizer = optim.AdamW(model.parameters(), lr=CONFIG['learning_rate'], weight_decay=CONFIG['weight_decay'])
    scheduler = optim.lr_scheduler.CosineAnnealingWarmRestarts(optimizer, T_0=3)
    scaler = GradScaler()
    best_val_jaccard = 0
    patience_counter = 0
    history = {
        'loss': [], 'val_loss': [],
        'bce_loss': [], 'val_bce_loss': [],
        'focal_loss': [], 'val_focal_loss': [],
        'ranking_loss': [], 'val_ranking_loss': [],
        'graph_kl_loss': [], 'val_graph_kl_loss': [],
        'jaccard': [], 'val_jaccard': []
    }

    train_batches = (len(train_texts) + CONFIG['batch_size'] - 1) // CONFIG['batch_size']
    val_batches = (len(val_texts) + CONFIG['batch_size'] - 1) // CONFIG['batch_size']

    for epoch in range(CONFIG['epochs']):
        model.train()
        train_loss = train_bce_loss = train_focal_loss = train_ranking_loss = train_graph_kl_loss = train_jaccard = 0
        valid_train_batches = 0

        train_pbar = tqdm(range(train_batches), desc=f"Epoch {epoch+1} Training", leave=True, ncols=90)
        for i in train_pbar:
            start = i * CONFIG['batch_size']
            end = min(start + CONFIG['batch_size'], len(train_texts))
            batch_texts = train_texts[start:end]
            batch_labels = train_labels[start:end]

            if not batch_texts or all(t == "[EMPTY]" for t in batch_texts) or not np.any(batch_labels):
                continue

            batch_labels = torch.tensor(batch_labels, dtype=torch.float32).to(device)

            optimizer.zero_grad()
            with autocast(device_type=device_type):
                try:
                    y_hat = model(batch_texts, labels=batch_labels)
                    loss, bce_loss, focal_loss, ranking_loss, graph_kl_loss = model.loss_fn(
                        y_hat, batch_labels, model.label_embeddings, model.loss_weight_net,
                        train_jaccard / max(valid_train_batches, 1)
                    )
                except Exception as e:
                    continue

            if torch.isnan(loss) or torch.isinf(loss):
                continue

            scaler.scale(loss).backward()
            scaler.unscale_(optimizer)
            torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=0.5)
            scaler.step(optimizer)
            scaler.update()

            preds = (y_hat.detach() > 0.5).float()
            batch_jaccard = jaccard_score(batch_labels.cpu().numpy(), preds.cpu().numpy(), average='samples', zero_division=0)

            train_loss += loss.item()
            train_bce_loss += bce_loss.item()
            train_focal_loss += focal_loss.item()
            train_ranking_loss += ranking_loss.item()
            train_graph_kl_loss += graph_kl_loss.item()
            train_jaccard += batch_jaccard
            valid_train_batches += 1

            # Update progress bar every 10 steps or last batch
            if valid_train_batches > 0 and ((i % 10 == 0) or (i == train_batches - 1)):
                avg_jaccard = train_jaccard / valid_train_batches
                train_pbar.set_postfix({'Avg Jaccard': f"{avg_jaccard:.4f}"})

            torch.cuda.empty_cache()

        if valid_train_batches > 0:
            train_loss /= valid_train_batches
            train_bce_loss /= valid_train_batches
            train_focal_loss /= valid_train_batches
            train_ranking_loss /= valid_train_batches
            train_graph_kl_loss /= valid_train_batches
            train_jaccard /= valid_train_batches

        # Validation
        model.eval()
        val_loss = val_bce_loss = val_focal_loss = val_ranking_loss = val_graph_kl_loss = val_jaccard = 0
        valid_val_batches = 0

        val_pbar = tqdm(range(val_batches), desc=f"Epoch {epoch+1} Validation", leave=True, ncols=90)
        with torch.no_grad():
            for i in val_pbar:
                start = i * CONFIG['batch_size']
                end = min(start + CONFIG['batch_size'], len(val_texts))
                batch_texts = val_texts[start:end]
                batch_labels = val_labels[start:end]

                if not batch_texts or all(t == "[EMPTY]" for t in batch_texts) or not np.any(batch_labels):
                    continue

                batch_labels = torch.tensor(batch_labels, dtype=torch.float32).to(device)

                with autocast(device_type=device_type):
                    try:
                        y_hat = model(batch_texts, labels=batch_labels)
                        loss, bce_loss, focal_loss, ranking_loss, graph_kl_loss = model.loss_fn(
                            y_hat, batch_labels, model.label_embeddings, model.loss_weight_net,
                            val_jaccard / max(valid_val_batches, 1)
                        )
                    except Exception as e:
                        continue

                preds = (y_hat.detach() > 0.5).float()
                batch_jaccard = jaccard_score(batch_labels.cpu().numpy(), preds.cpu().numpy(), average='samples', zero_division=0)

                val_loss += loss.item()
                val_bce_loss += bce_loss.item()
                val_focal_loss += focal_loss.item()
                val_ranking_loss += ranking_loss.item()
                val_graph_kl_loss += graph_kl_loss.item()
                val_jaccard += batch_jaccard
                valid_val_batches += 1

                if valid_val_batches > 0 and ((i % 10 == 0) or (i == val_batches - 1)):
                    avg_val_jaccard = val_jaccard / valid_val_batches
                    val_pbar.set_postfix({'Avg Jaccard': f"{avg_val_jaccard:.4f}"})

                torch.cuda.empty_cache()

        if valid_val_batches > 0:
            val_loss /= valid_val_batches
            val_bce_loss /= valid_val_batches
            val_focal_loss /= valid_val_batches
            val_ranking_loss /= valid_val_batches
            val_graph_kl_loss /= valid_val_batches
            val_jaccard /= valid_val_batches

        print(f"\nEpoch {epoch+1} Summary:")
        print(f"Train Loss: {train_loss:.4f} (BCE: {train_bce_loss:.4f}, Focal: {train_focal_loss:.4f}, Ranking: {train_ranking_loss:.4f}, Graph KL: {train_graph_kl_loss:.4f})")
        print(f"Train Jaccard: {train_jaccard:.4f}")
        print(f"Val Loss: {val_loss:.4f} (BCE: {val_bce_loss:.4f}, Focal: {val_focal_loss:.4f}, Ranking: {val_ranking_loss:.4f}, Graph KL: {val_graph_kl_loss:.4f})")
        print(f"Val Jaccard: {val_jaccard:.4f}")

        history['loss'].append(train_loss)
        history['val_loss'].append(val_loss)
        history['bce_loss'].append(train_bce_loss)
        history['val_bce_loss'].append(val_bce_loss)
        history['focal_loss'].append(train_focal_loss)
        history['val_focal_loss'].append(val_focal_loss)
        history['ranking_loss'].append(train_ranking_loss)
        history['val_ranking_loss'].append(val_ranking_loss)
        history['graph_kl_loss'].append(train_graph_kl_loss)
        history['val_graph_kl_loss'].append(val_graph_kl_loss)
        history['jaccard'].append(train_jaccard)
        history['val_jaccard'].append(val_jaccard)

        scheduler.step()

        if val_jaccard > best_val_jaccard:
            best_val_jaccard = val_jaccard
            if check_disk_space(CONFIG['results_dir']):
                torch.save(model.state_dict(), weights_file)
                print(f"Saved best model with Val Jaccard: {best_val_jaccard:.4f}")
                logger.info(f"Saved best model with Val Jaccard: {best_val_jaccard:.4f}")
            patience_counter = 0
        else:
            patience_counter += 1
            if patience_counter >= CONFIG['patience']:
                print(f"Early stopping at epoch {epoch+1}")
                logger.info(f"Early stopping at epoch {epoch+1}")
                break

        check_disk_space(CONFIG['results_dir'])

    if os.path.exists(weights_file):
        model.load_state_dict(torch.load(weights_file))
        print(f"Loaded best model from {weights_file}")
        logger.info(f"Loaded best model from {weights_file}")
    else:
        print(f"No saved model found at {weights_file}")
        logger.warning(f"No saved model found at {weights_file}")

    pd.DataFrame(history).to_csv(os.path.join(CONFIG['results_dir'], 'training_history.csv'), index=False)
    return model, history

