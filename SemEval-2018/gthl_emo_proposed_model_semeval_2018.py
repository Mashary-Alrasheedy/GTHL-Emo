def evaluate_classifier(model, texts, labels, split='test'):
    logger.info(f"Evaluating classifier on {split} split")
    print(f"Evaluating classifier on {split} split")
    model.eval()
    num_batches = (len(texts) + CONFIG['batch_size'] - 1) // CONFIG['batch_size']
    preds_proba = []
    span_attns = []
    input_ids_list = []

    with torch.no_grad():
        eval_pbar = tqdm(range(num_batches), desc=f"{split.capitalize()} Eval", leave=True, ncols=80)
        for i in eval_pbar:
            start = i * CONFIG['batch_size']
            end = min(start + CONFIG['batch_size'], len(texts))
            batch_texts = texts[start:end]
            if not batch_texts or all(t == "[EMPTY]" for t in batch_texts):
                continue
            with autocast(device_type=device_type):
                try:
                    y_hat, span_attn, input_ids = model(batch_texts, return_spans=True)
                    preds_proba.append(y_hat.cpu().float())
                    span_attns.append(span_attn.cpu())
                    input_ids_list.append(input_ids.cpu())
                except Exception as e:
                    continue
            torch.cuda.empty_cache()

    if not preds_proba:
        logger.warning(f"No valid predictions for {split} split")
        return {}, {}, np.zeros_like(labels), {}, {}

    preds_proba = torch.cat(preds_proba, dim=0).numpy()
    span_attn = torch.cat(span_attns, dim=0)
    input_ids = torch.cat(input_ids_list, dim=0)
    labels = np.array(labels)

    thresholds = np.arange(0.1, 0.5, 0.01)
    best_thresholds = {}
    for i, label in enumerate(CONFIG['emotion_labels']):
        best_jaccard = 0
        best_t = 0.5
        for t in thresholds:
            preds = (preds_proba[:, i] >= t).astype(int)
            j = jaccard_score(labels[:, i], preds, zero_division=0)
            if j > best_jaccard:
                best_jaccard = j
                best_t = t
        best_thresholds[label] = best_t

    preds = np.zeros_like(preds_proba)
    for i, label in enumerate(CONFIG['emotion_labels']):
        preds[:, i] = (preds_proba[:, i] >= best_thresholds[label]).astype(int)

    ap_scores = []
    no_positive_labels = []
    for i, label in enumerate(CONFIG['emotion_labels']):
        positive_count = np.sum(labels[:, i])
        logger.info(f"{split} set - {label}: {positive_count} positive samples")
        print(f"{split} set - {label}: {positive_count} positive samples")
        if positive_count == 0:
            logger.warning(f"No positive samples for label {label} in {split} set; skipping Average Precision")
            no_positive_labels.append(label)
            continue
        ap = average_precision_score(labels[:, i], preds_proba[:, i])
        if not np.isnan(ap):
            ap_scores.append(ap)

    metrics = {
        'jaccard': jaccard_score(labels, preds, average='samples', zero_division=0),
        'hamming_loss': hamming_loss(labels, preds),
        'precision': precision_score(labels, preds, average='samples', zero_division=0),
        'recall': recall_score(labels, preds, average='samples', zero_division=0),
        'f1': f1_score(labels, preds, average='samples', zero_division=0),
        'micro_precision': precision_score(labels, preds, average='micro', zero_division=0),
        'micro_recall': recall_score(labels, preds, average='micro', zero_division=0),
        'micro_f1': f1_score(labels, preds, average='micro', zero_division=0),
        'macro_precision': precision_score(labels, preds, average='macro', zero_division=0),
        'macro_recall': recall_score(labels, preds, average='macro', zero_division=0),
        'macro_f1': f1_score(labels, preds, average='macro', zero_division=0),
        'mean_average_precision': np.mean(ap_scores) if ap_scores else np.nan
    }

    class_metrics = {}
    confusion_matrices = {}
    for i, label in enumerate(CONFIG['emotion_labels']):
        class_metrics[label] = {
            'precision': precision_score(labels[:, i], preds[:, i], zero_division=0),
            'recall': recall_score(labels[:, i], preds[:, i], zero_division=0),
            'f1': f1_score(labels[:, i], preds[:, i], zero_division=0),
            'average_precision': average_precision_score(labels[:, i], preds_proba[:, i]) if np.sum(labels[:, i]) > 0 else np.nan,
            'threshold': best_thresholds[label]
        }
        cm = confusion_matrix(labels[:, i], preds[:, i])
        confusion_matrices[label] = cm

    return metrics, class_metrics, preds, best_thresholds, confusion_matrices

